let year = 1997;
switch(year % 12)
{
    case 0:
        console.log("Monkey");
        break;
    case 1:
        console.log("Cock");
        break;
    case 2:
        console.log("Dog");
        break;   
    case 3:
        console.log("Pig");
        break;
    case 4:
        console.log("Rat");
        break;
    case 5:
        console.log("Ox");
        break;
    case 6:
        console.log("Tiger");
        break;
    case 7:
        console.log("Rabbit");
        break;    
    case 8:
        console.log("Dragon");
        break;
    case 9:
        console.log("Snake");
        break;
    case 10:
        console.log("Horse");
        break;
    default:
        console.log("Goat");
        break;             
}